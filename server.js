// Serve uploaded and default files static directories
app.use('/uploads/defaults', express.static(DEFAULTS_DIR));
app.use('/uploads', express.static(UPLOADS_DIR));

// Configure Multer for PDF uploads
const storage = multer.diskStorage({
  destination: function (req, file, cb) {
    cb(null, UPLOADS_DIR);
  },
  filename: function (req, file, cb) {
    const ext = path.extname(file.originalname);
    const uniqueSuffix = Date.now() + '-' + Math.round(Math.random() * 1E9);
    cb(null, 'document-' + uniqueSuffix + (ext ? ext : '.pdf'));
  }
});

const upload = multer({
  storage: storage,
  fileFilter: function (req, file, cb) {
    // Only accept PDFs
    const ext = path.extname(file.originalname).toLowerCase();
    if (ext !== '.pdf') {
      return cb(new Error('Only PDF uploads are allowed!'));
    }
    cb(null, true);
  }
});

// --- API ROUTES ---

// Auth: Register
app.post('/api/auth/register', (req, res) => {
  const { name, email, password } = req.body;
  if (!name || !email || !password) {
    return res.status(400).json({ error: 'All fields are required' });
  }

  const dbData = readDB();
  const existingUser = dbData.users.find(u => u.email.toLowerCase() === email.toLowerCase());

  if (existingUser) {
    return res.status(400).json({ error: 'Email already registered' });
  }

  const newUser = {
    id: uuidv4(),
    name,
    email,
    password, // Store directly for simple mock db demonstration
    createdAt: new Date().toISOString()
  };

  dbData.users.push(newUser);
  writeDB(dbData);

  res.status(201).json({
    message: 'Registration successful',
    user: { id: newUser.id, name: newUser.name, email: newUser.email }
  });
});

// Auth: Login / Google Login
app.post('/api/auth/login', (req, res) => {
  const { email, password, isGoogleLogin, name } = req.body;

  if (!email) {
    return res.status(400).json({ error: 'Email is required' });
  }

  const dbData = readDB();
  let user = dbData.users.find(u => u.email.toLowerCase() === email.toLowerCase());

  if (isGoogleLogin) {
    // If Google login and user doesn't exist, create account on the fly (as requested: "student login kre gmail se agar uska pehle account createdd hai to , nhi to account create krke login kre")
    if (!user) {
      user = {
        id: uuidv4(),
        name: name || email.split('@')[0],
        email,
        password: 'OAuthMockPassword123',
        isGoogle: true,
        createdAt: new Date().toISOString()
      };
      dbData.users.push(user);
    }
  } else {
    // Normal email/password validation
    if (!user || user.password !== password) {
      return res.status(401).json({ error: 'Invalid email or password' });
    }
  }

  // Log the login event
  const loginLog = {
    id: uuidv4(),
    userId: user.id,
    name: user.name,
    email: user.email,
    timestamp: new Date().toISOString(),
    method: isGoogleLogin ? 'Google OAuth' : 'Email/Password'
  };
  dbData.loginLogs.unshift(loginLog); // Add to beginning
  writeDB(dbData);

  res.json({
    message: 'Login successful',
    user: { id: user.id, name: user.name, email: user.email }
  });
});

