app.get('/api/admin/users', (req, res) => {
  const dbData = readDB();
  // Return users summary (excluding passwords)
  const students = dbData.users.map(u => ({
    id: u.id,
    name: u.name,
    email: u.email,
    createdAt: u.createdAt,
    isGoogle: !!u.isGoogle
  }));
  res.json(students);
});

// Materials: Get all materials
app.get('/api/materials', (req, res) => {
  const dbData = readDB();
  res.json(dbData.materials);
});

// Materials: Upload a PDF (Admin)
app.post('/api/materials/upload', upload.single('file'), (req, res) => {
  const { title, subjectCode, category, subcategory } = req.body;

  if (!req.file) {
    return res.status(400).json({ error: 'Please upload a PDF file' });
  }
  if (!title || !subjectCode || !category) {
    return res.status(400).json({ error: 'Title, subject code, and category are required' });
  }

  const dbData = readDB();
  const newMaterial = {
    id: uuidv4(),
    title,
    subjectCode,
    category, // notes, papers, formulas, topics
    subcategory: subcategory || null, // e.g. mid-term-1, mid-term-2, end-term (used for papers)
    filename: req.file.filename,
    filepath: `/uploads/${req.file.filename}`,
    isDefault: false,
    uploadedAt: new Date().toISOString()
  };

  dbData.materials.push(newMaterial);
  writeDB(dbData);

  res.status(201).json({
    message: 'Material uploaded successfully',
    material: newMaterial
  });
});

// Materials: Log a download event (by a student)
app.post('/api/downloads', (req, res) => {
  const { name, email, subjectCode, title, filename } = req.body;
  if (!email || !filename) {
    return res.status(400).json({ error: 'Email and filename are required' });
  }

  const dbData = readDB();
  const downloadLog = {
    id: uuidv4(),
    name: name || 'Guest',
    email,
    subjectCode: subjectCode || 'N/A',
    title: title || filename,
    filename,
    timestamp: new Date().toISOString()
  };

  dbData.downloadLogs.unshift(downloadLog);
  writeDB(dbData);

  res.status(201).json({ message: 'Download logged successfully', log: downloadLog });
});

// Fallback error handler for multer
app.use((err, req, res, next) => {
  if (err instanceof multer.MulterError) {
    return res.status(400).json({ error: `Multer upload error: ${err.message}` });
  } else if (err) {
    return res.status(400).json({ error: err.message });
  }
  next();
});

// Start the server
app.listen(PORT, () => {
  console.log(`Server is running on port ${PORT}`);
  console.log(`Default materials seeded. Database at: ${DB_FILE}`);
});
